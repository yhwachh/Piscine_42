#include <unistd.h>

char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 65 && str[i] <= 90)
			str[i] += 32;
		i++;
	}
	if (str[0] >= 97 && str[0] <= 122)
		str[0] -= 32;
	i = 0;
	while (str[i])
	{
		if ((str[i] > 96 && str[i] < 123)
			&& ((str[i - 1] < 48 || (str[i - 1] > 57 && str[i - 1] < 65)
					|| (str[i - 1] > 90 && str[i - 1] < 97)
					|| str[i - 1] > 122)))
			str[i] -= 32;
		i++;
	}
	return (str);
}
